// Helper functions
export const getCountriesByContinent = (continent) => {
  return Object.entries(countries).filter(([_, country]) => country.continent === continent);
};

export const getRandomCountry = (continent = null) => {
  const filteredCountries = continent 
    ? getCountriesByContinent(continent)
    : Object.entries(countries);
  
  const randomIndex = Math.floor(Math.random() * filteredCountries.length);
  return filteredCountries[randomIndex];
};

export const getCountryByCode = (code) => {
  return countries[code];
};
